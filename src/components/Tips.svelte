		>How to make a good recording</summary
	>
	<h2>
		Read Me A Story makes it quick and easy for you to record a bedtime story. Follow these tips to
		ensure your recordings are crisp, clear and engaging, every time!
	</h2>

	<h3 class="my-4 underline decoration-amber-100 decoration-solid decoration-2 underline-offset-4">
		Know where your microphone is
	</h3>

	<p>
		If you're using a phone or tablet, your microphone is usually located on the the bottom of the
		handset or by the camera. If you're on a laptop or other device, make sure you select the
		correct audio source when prompted. To get the best results, keep your fingers, or any other
		obstructions, away from the mic and make sure it's pointing towards your face during the
		recording. Find a calm, quiet space to record A lot of background noise in your story can be
		distracting for a child. Try and find a quiet space to make your recordings.
	</p>

	<h3 class="my-4 underline decoration-amber-100 decoration-solid decoration-2 underline-offset-4">
		Minimise distractions
	</h3>

	<p>
		Before recording it's a good idea to mute your device, or enable 'Do not disturb' to avoid any
		notifications popping up while you record your story. Notifications can be distracting and their
		sound or vibrations will spoil a great bedtime story. Alternatively, you can enable airplane
		mode. Just don't forget to turn this off when you want to upload your story!
	</p>

	<h3 class="my-4 underline decoration-amber-100 decoration-solid decoration-2 underline-offset-4">
		Do a test recording
	</h3>

	<p>
		You can play back recordings before you upload them. It's a good idea to record the first few
		sentences of a story and play these back to check your setup and make any tweaks. When it sounds
		good, reset the recording and you're ready to go!
	</p>

	<h3 class="my-4 underline decoration-amber-100 decoration-solid decoration-2 underline-offset-4">
		Keep it interesting
	</h3>

	<p>
		Try using different voices for the characters in your story and vary your tone to help convey
		emotions. You can raise and lower your voice too, just make sure your microphone can handle it!
	</p>
</details>
